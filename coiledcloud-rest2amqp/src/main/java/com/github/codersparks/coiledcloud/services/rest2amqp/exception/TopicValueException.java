public class TopicValueException extends Exception {

	public TopicValueException() {
		
	}

	public TopicValueException(String arg0) {
		super(arg0);
	}

	public TopicValueException(Throwable arg0) {
		super(arg0);
	}

	public TopicValueException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TopicValueException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
